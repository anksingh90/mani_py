#Positive, Negative, or Zero: Write a program that asks the user for a number.
#Print "Positive" if the number is greater than 0, "Negative" if the number is less
#than 0, and "Zero" if the number is equal to 0.

a=int(input("Enter the number: "))
if a>0:
    print("positive")
elif a<0:
    print("negative")
elif a==0:
    print("zero") 
